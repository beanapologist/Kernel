#!/usr/bin/env python3
"""
upload_hdawg.py — Upload waveforms to Zurich Instruments HDAWG.

This script demonstrates how to connect to an HDAWG, configure the
sequencer for direct waveform playback, and upload the I/Q arrays
generated by generate_waveforms.py.

PREREQUISITES
-------------
- zhinst-toolkit installed:   pip install zhinst-toolkit
- LabOne or HDAWG firmware running and reachable at the host/port below.
- Fill in DEVICE_SERIAL and channel mapping constants before use.

USAGE
-----
  python upload_hdawg.py --config config_example.yaml --waveform waveforms.npz

The script:
  1. Loads I_pal, Q_pal (palindromic) from the NPZ file.
  2. Connects to the HDAWG via zhinst.toolkit.
  3. Uploads the waveform table to the chosen channel pair.
  4. Arms the AWG core so the waveform plays on the next trigger.

For the three-run protocol, repeat with --variant ctrl for the control run.
"""

import argparse
import sys
from pathlib import Path

import numpy as np

# ---------------------------------------------------------------------------
# Device configuration — edit these or supply via config_example.yaml
# ---------------------------------------------------------------------------
DEFAULT_DEVICE_SERIAL = "DEVXXXXX"   # <-- replace with your device serial
DEFAULT_HOST = "localhost"
DEFAULT_PORT = 8004
# HDAWG has 8 channels (4 pairs).  Channel pair 0 → channels 1 & 2.
DEFAULT_CHANNEL_PAIR = 0             # 0 = channels 1/2, 1 = 3/4, etc.

# ---------------------------------------------------------------------------
# Minimal sequencer program for direct waveform playback
# ---------------------------------------------------------------------------
SEQUENCER_PROGRAM = """\
// Minimal AWG sequence: play waveform once on external trigger.
wave w_I = placeholder({n_samples}, true);
wave w_Q = placeholder({n_samples}, true);
while (true) {{
  waitDigTrigger(1);          // wait for software/hardware trigger
  playWave(w_I, w_Q);
  waitWave();
}}
"""


def load_config(config_path: str) -> dict:
    """Load YAML configuration (optional; falls back to defaults)."""
    try:
        import yaml
    except ImportError:
        print("[warn] pyyaml not installed — using defaults for device config.")
        return {}
    with open(config_path) as fh:
        return yaml.safe_load(fh) or {}


def load_waveforms(npz_path: str, variant: str = "pal") -> tuple[np.ndarray, np.ndarray]:
    """
    Load I/Q arrays from the NPZ file produced by generate_waveforms.py.

    Parameters
    ----------
    npz_path : path to the .npz file
    variant  : 'pal' or 'ctrl'

    Returns
    -------
    I_arr, Q_arr : float64 arrays, values in [-1, 1]
    """
    data = np.load(npz_path, allow_pickle=True)
    i_key = f"I_{variant}"
    q_key = f"Q_{variant}"
    if i_key not in data:
        raise KeyError(
            f"Key '{i_key}' not found in {npz_path}. "
            f"Available keys: {list(data.keys())}"
        )
    return data[i_key].astype(np.float64), data[q_key].astype(np.float64)


def upload_to_hdawg(
    device_serial: str,
    host: str,
    port: int,
    channel_pair: int,
    I_arr: np.ndarray,
    Q_arr: np.ndarray,
) -> None:
    """
    Connect to the HDAWG and upload the waveform.

    Raises ImportError if zhinst-toolkit is not installed.
    """
    try:
        from zhinst.toolkit import Session  # type: ignore
    except ImportError as exc:
        raise ImportError(
            "zhinst-toolkit is required to upload waveforms. "
            "Install with:  pip install zhinst-toolkit"
        ) from exc

    n_samples = len(I_arr)
    print(f"Connecting to {device_serial} at {host}:{port} …")
    session = Session(host, port)
    device = session.connect_device(device_serial)

    awg = device.awgs[channel_pair]

    # Upload sequencer program
    seq_program = SEQUENCER_PROGRAM.format(n_samples=n_samples)
    print(f"Compiling sequencer program ({n_samples} samples) …")
    awg.load_sequencer_program(seq_program)

    # Upload waveform to index 0 in the waveform table
    # zhinst-toolkit ≥ 0.5: use replace_waveform / assign_waveform
    print("Uploading waveform …")
    awg.write_to_waveform_memory(0, [I_arr, Q_arr])

    # Arm the AWG (enable; it will wait for trigger)
    awg.enable(True)
    print("AWG armed.  Send a trigger to play the waveform.")


def main(argv: list[str] | None = None) -> None:
    parser = argparse.ArgumentParser(
        description="Upload HDAWG waveforms for Passive GHz Demo Variant 1."
    )
    parser.add_argument(
        "--config",
        type=str,
        default="config_example.yaml",
        help="Path to YAML config file (default: config_example.yaml).",
    )
    parser.add_argument(
        "--waveform",
        type=str,
        default="waveforms.npz",
        help="Path to waveforms NPZ file (default: waveforms.npz).",
    )
    parser.add_argument(
        "--variant",
        choices=["pal", "ctrl"],
        default="pal",
        help="Which waveform variant to upload: 'pal' or 'ctrl' (default: pal).",
    )
    args = parser.parse_args(argv)

    cfg = load_config(args.config) if Path(args.config).exists() else {}

    device_serial = cfg.get("device_id", DEFAULT_DEVICE_SERIAL)
    host = cfg.get("host", DEFAULT_HOST)
    port = int(cfg.get("port", DEFAULT_PORT))
    channel_pair = int(cfg.get("channel_pair", DEFAULT_CHANNEL_PAIR))

    if device_serial == DEFAULT_DEVICE_SERIAL:
        print(
            f"[warn] Using placeholder device serial '{DEFAULT_DEVICE_SERIAL}'. "
            "Set 'device_id' in config_example.yaml before running on real hardware."
        )

    print(f"Loading variant='{args.variant}' from {args.waveform} …")
    I_arr, Q_arr = load_waveforms(args.waveform, variant=args.variant)
    print(f"  Loaded {len(I_arr)} samples (I) and {len(Q_arr)} samples (Q).")

    upload_to_hdawg(
        device_serial=device_serial,
        host=host,
        port=port,
        channel_pair=channel_pair,
        I_arr=I_arr,
        Q_arr=Q_arr,
    )


if __name__ == "__main__":
    main()
